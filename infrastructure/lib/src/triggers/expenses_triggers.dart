      '   expense_id,'
      '   description,'
      '   value,'
      '   date,'
      '   payment_method_id,'
      '   subcategory_id,'
      '   store_id,'
      '   altered_at'
      ' ) '
      ' values('
      '   old.id,'
      '   old.description,'
      '   old.value,'
      '   old.date,'
      '   old.payment_method_id,'
      '   old.subcategory_id,'
      '   old.store_id,'
      '   old.updated_at'
      ' );'
      'END;';
}
